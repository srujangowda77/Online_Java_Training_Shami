package JavaPackage;

public class Variable1 
{
	
	
	public static void main(String[] args) 
	{
		int a= 100;        //Local variable
	
		System.out.println(a);
		
		char b = 's';
		
		System.out.println(b);
		
		boolean c= true;
		c = false;
		
		System.out.println(c);
		
		boolean d = 500 > 300 ;
		
		System.out.println(d);
		
	}

}
